#include "libft.h"

void	ft_putchar_fd(int c, int fd)
{
	char	ds[3];

	if (c < 0x80)
		write(fd, &c, 1);
	else if (c < 2048)
	{
		ds[0] = 0xc0 + (c / 64);
		ds[1] = 0x80 + (c % 64);
		write(fd, ds, 3);
	}
	else
	{
		ds[0] = 0xe0 + (c / 4096);
		c -= (c / 2048) * 2048;
		ds[1] = 0xa0 + (c / 64);
		ds[2] = 0x80 + (c % 64);
		write(fd, ds, 3);
	}
}
